package com.FirstProgram;

public class Swaping_By_Temp {

    public static void main (String [] args){
        int a = 5;
        int b = 4;
        int temp;

        temp = a; //temp = 5
        a = b;  //a = 4
        b = temp; //b = 5

        System.out.println("a : " + a);
        System.out.println("b : " + b);



    }
}
